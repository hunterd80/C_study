package study;

public class 문제6_1_1 
{
	public static void main(String[] args)
	{
		setNumber(5,3);		
	}
	
	public static void setNumber(int a, int b)
	{
		System.out.println("더하기는"+(a+b));
		System.out.println("빼기는"+(a-b));
		System.out.println("곱하기는"+(a*b));
		System.out.println("나누기는"+(a/b));
		System.out.println("나머지는"+(a%b));
	}
	

}
